    private void botaoRegistarVacActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRegistarVacActionPerformed
        try {
            String item = String.valueOf(comboUtente.getSelectedItem());
            String item2 = String.valueOf(comboVacina.getSelectedItem());
            Utente utente= (Utente) sistema.getListaUtilizadores().getUtilizador(item);
            Vacina vacina = (Vacina) sistema.getListaVacinas().getVacinaNome(item2);
            if (centro.getMarcacoes().ConsultarMarcacao(LocalDate.now()).contains(utente)){
                centro.inoculacao(utente, vacina, txtEfeitos.getText());
                JOptionPane.showMessageDialog(this, "Inoculação registada com sucesso!");
                dispose();
                       
            }else {
                JOptionPane.showMessageDialog(this, "Utente não tem marcacao para hoje!");
            }
            if(utente.getNumTomas()< vacina.getNumTomas()){
                GestorProcMarc p = new GestorProcMarc(utente, sistema, bd, centro);
                p.setVisible(true);
            }
        } catch (ListaUtilizadores.UtilizadorNaoExistenteException | ListaVacinas.VacinaNaoExistenteException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
            
     
    
    }//GEN-LAST:event_botaoRegistarVacActionPerformed

