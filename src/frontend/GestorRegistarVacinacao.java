    private void carregarListaUtentes(){
        comboUtente.removeAllItems();
        if (sistema.getListaUtilizadores().listarUtentesCentro(centro)!=null){
            for (Utente utente: sistema.getListaUtilizadores().listarUtentesCentro(centro)){
                String item =String.valueOf(utente.getUsername());
                comboUtente.addItem(item);
            }
        }
    }
    
    private void carregarListaVacinas() throws CentroNaoExistenteException{
        comboVacina.removeAllItems();
        String codigo = centro.getCodigo();
        if (sistema.getListaCentros().getCentroVacinacao(codigo).getStockVacinas().getStock()!=null){
            for (Vacina vacinas: sistema.getListaCentros().getCentroVacinacao(codigo).getStockVacinas().getStock().keySet()){
                String item2 =String.valueOf(vacinas.getNomeVacina());
                comboVacina.addItem(item2);
            }
        }
          
    }    

