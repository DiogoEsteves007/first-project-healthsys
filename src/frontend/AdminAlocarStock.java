    private void carregarListaVacinas(){
        comboVacinas.removeAllItems();
        for(Object vacina : sistema.getListaVacinas().getListaVacinas()){
            Vacina vac = (Vacina) vacina;
            String item = String.valueOf(vac.getNomeVacina());
            comboVacinas.addItem(item);
        }
    }

    private void carregarTabelaStock(){
        model.setRowCount(0); //especifica o nr de linhas na tabela
        for (Vacina vac : centro.getStockVacinas().getStock().keySet()){
            model.addRow(new Object[] {vac.getCodVacina(), vac.getNomeVacina(), centro.getStockVacinas().qtdVacina(vac)});
        }
        tabelaStock.setModel(model);
    }
    
    
    private void remover() throws ListaVacinas.VacinaNaoExistenteException{
        if (!txtQuantidade.getText().isEmpty()){
            int qtd=Integer.parseInt(txtQuantidade.getText());
            String item = String.valueOf(comboVacinas.getSelectedItem());
            Vacina vac = sistema.getListaVacinas().getVacinaNome(item);
            
            if(centro.getStockVacinas().existeVacina(vac)){           
                centro.getStockVacinas().removerStock(vac, qtd);
            }
          
            JOptionPane.showMessageDialog(this, "Vacina removida com sucesso!");
            carregarTabelaStock();
            guardarAlteracoes();
        }else{
            JOptionPane.showMessageDialog(this, "Introduza uma quantidade!");
        }  
    }
    
    private void adicionar() throws ListaVacinas.VacinaNaoExistenteException{
        if (!txtQuantidade.getText().isEmpty()){
            int qtd=Integer.parseInt(txtQuantidade.getText());
            String item = String.valueOf(comboVacinas.getSelectedItem());
            Vacina vac = sistema.getListaVacinas().getVacinaNome(item);
            
            if(centro.getStockVacinas().existeVacina(vac)){
                int qtdAtual=centro.getStockVacinas().qtdVacina(vac);
                centro.getStockVacinas().criarStock(vac, qtd+qtdAtual);
            }else{
                centro.getStockVacinas().criarStock(vac, qtd);
            }
            
            JOptionPane.showMessageDialog(this, "Vacina adicionada com sucesso!");
            carregarTabelaStock();
            guardarAlteracoes();
        }else{
            JOptionPane.showMessageDialog(this, "Introduza uma quantidade!");
        }       
    }
    
    private void guardarAlteracoes() {
        bd.gravaSistema(sistema);
    }
 
