    private void botaoGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoGuardarActionPerformed
        ListaVacinas listaTemp= new ListaVacinas();
        
        for (int index=0; index<tabelaVacinas.getRowCount(); index++){
            int column = 0;
            int column1= 1;
            int column2= 2;
            int column3= 3;
            int column4= 4;
            int column5= 5;
            int column6= 6;
            int column7= 7;
            int column8= 8;
            String codigo = tabelaVacinas.getModel().getValueAt(index, column).toString();
            String nome = tabelaVacinas.getModel().getValueAt(index, column1).toString();
            String fabricante = tabelaVacinas.getModel().getValueAt(index, column2).toString();
            float dose = Float.parseFloat(tabelaVacinas.getModel().getValueAt(index, column3).toString());
            String rest =tabelaVacinas.getModel().getValueAt(index, column4).toString();
            int tomas =Integer.parseInt(tabelaVacinas.getModel().getValueAt(index, column5).toString());
            int min =Integer.parseInt(tabelaVacinas.getModel().getValueAt(index, column6).toString());
            int max =Integer.parseInt(tabelaVacinas.getModel().getValueAt(index, column7).toString());
            String ef = tabelaVacinas.getModel().getValueAt(index, column8).toString();
            String lista[] = ef.split(",");
            ArrayList<String> efeitos = new ArrayList<>(Arrays.asList(lista));

            
            try {
                listaTemp.adicionarVacina(new Vacina (codigo, nome, fabricante, dose, rest, tomas, min, max, (ArrayList) efeitos));
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
        sistema.setListaVacinas(listaTemp);
        JOptionPane.showMessageDialog(this, "Alterações guardadas com sucesso!");
        preencheTabela();    
        

    }//GEN-LAST:event_botaoGuardarActionPerformed

    private void botaoRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRemoverActionPerformed
        //remover o gestor da linha selecionada
        int column = 0;
        int row = tabelaVacinas.getSelectedRow();
        String value = tabelaVacinas.getModel().getValueAt(row, column).toString();
        sistema.getListaUtilizadores().removerUtilizador(value);
        //remover linha da tabela
        if (tabelaVacinas.getSelectedRow() != -1) {
            model.removeRow(tabelaVacinas.getSelectedRow());
        }
    }//GEN-LAST:event_botaoRemoverActionPerformed

