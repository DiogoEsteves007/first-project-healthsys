    private void preencheTabela() {
        model.setRowCount(0); //especifica o nr de linhas na tabela
        for (int index = 0; index < sistema.getListaVacinas().getListaVacinas().size(); index++) {
            Vacina vac = sistema.getListaVacinas().getListaVacinas().get(index);
            model.addRow(new Object[]{vac.getCodVacina(), vac.getNomeVacina(), vac.getFabricante(), vac.getDoseIndividual(), 
                vac.getRestricoes(), vac.getNumTomas(), vac.getPrazoMin(), vac.getPrazoMax(), String.join(",", vac.getEfeitosSecundarios())});   

        }
        tabelaVacinas.setModel(model);
    }
