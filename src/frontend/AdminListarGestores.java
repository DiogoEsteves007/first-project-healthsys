    private void preencheTabela() {
        modelGestores.setRowCount(0); //especifica o nr de linhas na tabela
        for (int index = 0; index < sistema.getListaUtilizadores().listagemGestores().size(); index++) {
            Gestor g =sistema.getListaUtilizadores().listagemGestores().get(index);
            modelGestores.addRow(new Object[]{g.getUsername(), g.getPassword()});
        }
        tabelaGestores.setModel(modelGestores);
 }
