    private void botaoApagarGestorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoApagarGestorActionPerformed
        //remover linha da tabela
        if (tabelaGestores.getSelectedRow() != -1) {           
            //remover o gestor da linha selecionada
            int column = 0;
            int row = tabelaGestores.getSelectedRow();
            String value = tabelaGestores.getModel().getValueAt(row, column).toString();
            try {
                Gestor g= (Gestor) sistema.getListaUtilizadores().getUtilizador(value);
                CentroVacinacao centro= g.getCentroAtribuido();
                centro.setGestor(null);
                sistema.getListaUtilizadores().removerUtilizador(value);
                modelGestores.removeRow(tabelaGestores.getSelectedRow());
            } catch (ListaUtilizadores.UtilizadorNaoExistenteException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Selecione um gestor!");
        }
    }//GEN-LAST:event_botaoApagarGestorActionPerformed

    private void botaoAdicionarGestorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAdicionarGestorActionPerformed
        //modelGestores.addRow(new Object[]{"", null});
        if (txtUser.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza o nome de utilizador!");            
            txtUser.requestFocus();
            return;
        }
        
        if (txtSenha.getPassword().length == 0) {
            JOptionPane.showMessageDialog(this, "Introduza a password!");
            txtSenha.requestFocus();
            return;
        } 
        
        String user= txtUser.getText();
        String passe =txtSenha.getText();
        try {
            sistema.getListaUtilizadores().adicionarUtilizador(new Gestor(user, passe));
        } catch (ListaUtilizadores.UtilizadorDuplicadoException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
        preencheTabela();
        

    }//GEN-LAST:event_botaoAdicionarGestorActionPerformed

    private void botaoAllterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAllterarActionPerformed
        if (txtUser.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza o nome do gestor que quer editar!");            
             txtUser.requestFocus();
            return;
        }
        
        if (txtSenha.getPassword().length == 0) {
                JOptionPane.showMessageDialog(this, "Introduza uma password!");
                txtSenha.requestFocus();
                return;
            }  
        if (sistema.getListaUtilizadores().existe(txtUser.getText())){
            Gestor g;
            try {
                g = (Gestor) sistema.getListaUtilizadores().getUtilizador(txtUser.getText());
                g.setPassword(txtSenha.getText());
            } catch (ListaUtilizadores.UtilizadorNaoExistenteException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }            
            preencheTabela();
            
        }else{
            JOptionPane.showMessageDialog(this, "Esse gestor não existe!"); 
        }

    }//GEN-LAST:event_botaoAllterarActionPerformed

