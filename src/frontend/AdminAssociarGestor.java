    private void carregarListaGestores(){
        comboGestores.removeAllItems();
        for (Object gestor: sistema.getListaUtilizadores().gestoresLivres()){
            Gestor g= (Gestor) gestor;
            String item =String.valueOf(g.getUsername());
            comboGestores.addItem(item);
        }
    }
    private void atribuir() throws ListaUtilizadores.UtilizadorNaoExistenteException{
        String item = String.valueOf(comboGestores.getSelectedItem());
        //String item= txtGestor.getText();
        Gestor gestor =(Gestor)sistema.getListaUtilizadores().getUtilizador(item);
        
        if (centro.getGestor()==null) {
            centro.setGestor(gestor);
            gestor.setCentroAtribuido(centro);
            //centro.setMarcacoes(new AgendaMarcacoes());
            JOptionPane.showMessageDialog(this, "Gestor atribuido com sucesso!");
            dispose();
            
        }
    }
